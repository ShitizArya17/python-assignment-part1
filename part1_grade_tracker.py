student_name = "Ayesha Sharma"
subjects     = ["Math", "Physics", "CS", "English", "Chemistry"]
marks        = [88, 72, 95, 60, 78]

# Printing each subject with its grade
print(f"\nGrade report for {student_name}:")
print("-" * 35)

for subject, mark in zip(subjects, marks):
    if mark >= 90:
        grade = "A+"
    elif mark >= 80:
        grade = "A"
    elif mark >= 70:
        grade = "B"
    elif mark >= 60:
        grade = "C"
    else:
        grade = "F"

    # f-string with alignment: :<15 means left-align in 15 characters, :>3 right-align in 3
    print(f"  {subject:<12} | Marks: {mark:>3} | Grade: {grade}")

# Calculating statistics
total   = sum(marks)
average = round(total / len(marks), 2)
highest_mark = max(marks)
highest_idx  = marks.index(highest_mark)
highest_subj = subjects[highest_idx]
lowest_mark  = min(marks)
lowest_idx   = marks.index(lowest_mark)
lowest_subj  = subjects[lowest_idx]

print(f"\nTotal Marks    : {total}")
print(f"Average Marks  : {average}")
print(f"Highest Subject: {highest_subj} ({highest_mark})")
print(f"Lowest Subject : {lowest_subj} ({lowest_mark})")

# Simulate a marks-entry system

print("\n--- Add New Subjects (type 'done' to stop) ---")
new_count = 0  # counter for how many valid new subjects were added

while True:  # True means loop forever, using 'break' to stop it
    # Ask the user to type a subject name
    new_subject = input("Enter subject name (or 'done' to finish): ").strip()
    if new_subject.lower() == "done":
        break
    marks_input = input(f"Enter marks for {new_subject} (0-100): ").strip()
 # Managing Invalid input
    try:
        new_mark = int(marks_input)

        if 0 <= new_mark <= 100:
            subjects.append(new_subject)
            marks.append(new_mark)
            new_count += 1
            print(f"  ✓ Added {new_subject}: {new_mark}")
        else:
            print(f"  do not crash, and do not add invalid entries to the list. '{new_mark}' ignored.")

    except ValueError:
        print(f"  ⚠ Warning: '{marks_input}' do not crash, and do not add invalid entries to the list. Ignored.")

updated_total   = sum(marks)
updated_average = round(updated_total / len(marks), 2)
print(f"\n{new_count} new subject(s) added.")
print(f"Updated average across all subjects: {updated_average}")

