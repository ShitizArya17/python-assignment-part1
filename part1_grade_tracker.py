raw_students = [
    {"name": "  ayesha SHARMA  ", "roll": "101", "marks_str": "88, 72, 95, 60, 78"},
    {"name": "ROHIT verma",       "roll": "102", "marks_str": "55, 68, 49, 72, 61"},
    {"name": "  Priya Nair  ",    "roll": "103", "marks_str": "91, 85, 88, 94, 79"},
    {"name": "karan MEHTA",       "roll": "104", "marks_str": "40, 55, 38, 62, 50"},
    {"name": " Sneha pillai ",    "roll": "105", "marks_str": "75, 80, 70, 68, 85"},
]

cleaned_students = []

print("*" * 40)
print("TASK 1 — Data Parsing & Profile Cleaning")
print("*" * 40)

for student in raw_students:

    # Cleaning the name formating
    clean_name = student["name"].strip().title()

    # Converting roll number from string to integer
    clean_roll = int(student["roll"])

    # Converting marks string to a list of integers
    clean_marks = [int(m) for m in student["marks_str"].split(", ")]

    # Validating the name
    # .isalpha() returns True if ALL characters in a string are letters
    # all([...]) returns True only if EVERY item in the list is True
    words = clean_name.split()
    name_is_valid = all(word.isalpha() for word in words)

    # Print valid/invalid message next to each student
    if name_is_valid:
        validity_msg = "✓ Valid name"
    else:
        validity_msg = "✗ Invalid name"

    # Build a new cleaned student dictionary and add it to our list
    cleaned = {
        "name":  clean_name,
        "roll":  clean_roll,
        "marks": clean_marks
    }
    cleaned_students.append(cleaned)

    # Printing the profile card
    print(f"\n================================")
    print(f"Student : {cleaned['name']}    {validity_msg}")
    print(f"Roll No : {cleaned['roll']}")
    print(f"Marks   : {cleaned['marks']}")
    print(f"================================")

# Print name in ALL CAPS for roll number 103
print("\n--- Special case for Roll No 103 ---")
for student in cleaned_students:
    if student["roll"] == 103:
        print(f"{student['name'].upper()}")

